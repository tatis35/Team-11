package day04_varriables;

public class C05_Incrementvariables {

    public static void main(String[] args) {


        int numA=2;

        numA += 3;
        System.out.println(numA);

        int numB=10;

        numB *=7;

        System.out.println(numB);

        int numC=7;

        numC++;
        System.out.println(numC);

        int numA2=2;

        numA2 -=3;
        System.out.println(numA2);

        int numB2=20;

        numB2 /=5;

        System.out.println(numB2);

        int numC2=7;

        numC2--;

        System.out.println(numC2);




    }
}
